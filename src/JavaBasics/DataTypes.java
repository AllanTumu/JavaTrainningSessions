package JavaBasics;

public class DataTypes {

    //main() method --> starting/executing point of the program

    public static void main (String[] args) {

        //int
        int a = 10;
        a = 20;
        System.out.println(a);

        //double
        double d = 12.33;
        double s = 100;
        System.out.println(s);

//        characters --> They should be written in single quotes
//        Characters should be single digit values
        char c = 'a';
        System.out.println(c);

        //boolean:
        boolean b1 = true;
        boolean b2 = false;

        //String: A string is a class not data type
        //Strings should be in double quotes
        String s1 = "Hello world";

    }

}
